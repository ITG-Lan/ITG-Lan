                  <p>
                    Vi är åtta studenter inom IT- och el-programmet som valt att som gymnasiearbete engagera oss i ett antal evenemang för ungdomar i Västerås.
                    Slutprojektet kommer vara under valborg som ett alternativ till ungdomar som inte vill vara ute bland alkohol och droger.
                    LAN:et är 100% alkohol- och drog-fritt (se regler).
                  </p>
                  <p>
                    LAN (Local Area Network) är ett evenemang där deltagande personer har med sig sin dator och kopplar upp sig mot samma nätverk för att spela tillsammans.
                    Vanligtvis är dessa mindre evenemang som detta, men det finns även större datorfestivaler som t.ex Dreamhack.
                    Ofta håller eventet på under några dagar (och nätter) där personerna umgås och träffar vänner.
                  </p>
                  <p>
                    Tidigare under läsåret har vi hållit i två mindre evenemang och det sista kommer att vara under valborgshelgen.
                    Vid skolans LAN evenemang finns alltid minst två lärare på plats samt en grupp elever som ser till att allting fungerar som det ska samt att alla trivs
                    under evenemanget. Vid ingång på LAN:et lämnas en depositionsavgift på 50 kronor till ansvarande elever som bevaras säkert
                    tills eleven lämnar LAN:et med en ren och städad plats. Om eleven inte städat sin plats när den lämnar LAN:et betalas inte avgiften tillbaka till eleven.
                  </p>
                  <p>
                    Området övervakas av Securitas och om dörrar hålls öppna under en längre tid går ett larm och väktare blir tvingade att ta sig till lokalerna.
                    Eleven / eleverna som ansvarar för utryckningen blir ansvariga för eventuella kostnader.
                  </p>
                  <p>
                    Evenemanget hålls i skolans lokaler på Ingenjör Bååths Gata 47 i Västerås.
                    Skolan ligger väldigt centralt i Västerås med ungefär två minuters promenad från centralstationen.
                    Olika resturanger finns bara några minuter från skolområdet och det finns även närliggande affärer.
                    Kylskåp kommer finnas för medhavande matlådor eller dryck. Det finns begränsad plats i kylskåpet.
                    Det betyder att vi måste prioritera matlådor före t.ex läsk eller godis.
                    På kylskåpet sitter ett lås och bara lärare och ansvarande elever har tillgång till nyckel.
                  </p>
